extension Sorting on List<int> {
  List<int> sortAsc() {
    var list = this;
    var length = this.length;

    for (int i = 0; i < length - 1; i++) {
      int min = i;
      for (int j = i + 1; j < length; j++) {
        if (list[j] < list[min]) {
          min = j;
        }
      }

      int tmp = list[min];
      list[min] = list[i];
      list[i] = tmp;
    }

    return list;
  }
}

main(List<String> args) {
  var unSorted = [2, 5, 3, 1, 4];
  print(unSorted);
  var sorted = unSorted.sortAsc();
  print(sorted);
}
